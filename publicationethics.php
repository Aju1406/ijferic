                              <div><p>IRJMETS Journal encourage the best standards of publication ethics 
and take all possible measures against publication malpractices. These 
guidelines are fully consistent with the COPE Principles of Transparency
 and Best Practice Guidelines and the COPE Code of Conduct. More details
 can be found here: https://publicationethics.org.</p><p>
</p><p>As an essential issue, publication ethics needs to be explained 
clearly to improve the quality of the research worldwide. In this part, 
we explain the standard for editors, authors, and reviewers. Publisher 
don’t have right to interfere with the integrity of the contents and 
only support to publish in timely manner.
.</p>
<h4 class="text-left">EDITORIAL RESPONSIBILITIES</h4>
		<ul class="list"><ul><li>1- The Editors’ decisions to accept or reject a manuscript for 
publication should be based on the importance, originality, clarity, and
 relevance of the paper. Editors should process manuscripts submitted in
 a timely manner. </li><li>
<br></li><li>2- Editors should ensure that appropriate reviewers are 
selected for submissions (i.e. individuals with relevant expertise 
capable of critically evaluating the manuscript, who have no competing 
interests). Editors highlight the need for all reviewers to be objective
 in their evaluation. </li><li>
<br></li><li>3- Any manuscripts received for review must be treated as 
confidential documents. They must not be shown to or discussed with 
others except as authorized by the editor. </li><li>
<br></li><li>4- The editor should be responsible for deciding which of the articles submitted to the journal should be published. </li><li>
<br></li><li>5- The editor may be guided by the policies of the journal's 
editorial board and constrained by such legal requirements, copyright 
infringement and plagiarism. </li><li>
<br></li><li>6- Editors should have no potential conflicts of interest with 
regard to the manuscript (with respect to the research, authors and/or 
research funders). </li><li>
<br></li><li>7- Editors and members of the editorial staff should not reveal
 the identity of reviewers prior to or following publication of the 
manuscript. </li><li>
<br></li></ul><h4 class="text-center">DUTIES AND RESPONSIBILITIES OF REVIEWERS</h4><li>1- Peer review assists the editor in making editorial decisions and 
through the editorial communications with the author may also assist the
 author in improving the paper. </li><li>
<br></li><li>2- Peer review is an essential component of formal scholarly 
communication. Authors who wish to contribute to publications have an 
obligation to do a fair share of reviewing. </li><li>
<br></li><li>3- Reviewers need to comment on ethical questions and possible research and publication misconduct. </li><li>
