				<h4 class="text-left">DEALING WITH POTENTIAL MISCONDUCT</h4>	
				<p>Editors have a duty to take action if they suspect misconduct or 
if an allegation of misconduct is notified to them. This duty extends to
 both published and unpublished papers. Editors should not merely reject
 papers that raise concerns over potential misconduct. They are 
ethically obliged to pursue alleged cases.Editors should first seek a 
response from those suspected of misconduct. If they are not satisfied 
with the response, they should ask the relevant employers, or 
institution, or other appropriate body to investigate. Editors should 
make all reasonable efforts to ensure that a proper investigation into 
alleged misconduct is conducted; should the latter not take place, 
editors should make all reasonable attempts to obtain a resolution to 
the problem.
</p>
				<h4 class="text-left">PLAGIARISM</h4>	
				<p>Manuscript submissions are checked for intellectual property 
protection and document-versus-document(s) analysis to satisfy the 
Editor that the submitted manuscript has not been plagiarised. However, 
when authors submit their manuscripts for consideration in the Journal, 
they declare that their work is not plagiarised. While the Editor makes 
reasonable efforts to determine the academic integrity of papers 
published in the Journal, ultimate responsibility for the originality of
 submitted manuscripts thus lies with the author.</p><p>
				    
</p><p>Plagiarism takes place when one author deliberately uses 
another’s work without permission, credit, or acknowledgment. Authors 
must always remember that crediting the work of others (including your 
advisor’s or your own previous work) is paramount. Authors should always
 place their work in the context of the advancement of the field, and 
acknowledge the findings of others on which you have built your 
research.
</p>
