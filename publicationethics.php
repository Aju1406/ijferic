                    <div class="post-content" align="justify">
                         <p class="post-desc" align="justify"> 
                              <div><p>IJFER Journal encourage the best standards of publication ethics 
and take all possible measures against publication malpractices. These 
guidelines are fully consistent with the COPE Principles of Transparency
 and Best Practice Guidelines and the COPE Code of Conduct. More details
 can be found here: https://publicationethics.org.</p><p>
</p><p>As an essential issue, publication ethics needs to be explained 
clearly to improve the quality of the research worldwide. In this part, 
we explain the standard for editors, authors, and reviewers. Publisher 
don’t have right to interfere with the integrity of the contents and 
only support to publish in timely manner.
.</p>
<h4 class="text-left">EDITORIAL RESPONSIBILITIES</h4>
		<ul class="list"><ul><li>1- The Editors’ decisions to accept or reject a manuscript for 
publication should be based on the importance, originality, clarity, and
 relevance of the paper. Editors should process manuscripts submitted in
 a timely manner. </li><li>
<br></li><li>2- Editors should ensure that appropriate reviewers are 
selected for submissions (i.e. individuals with relevant expertise 
capable of critically evaluating the manuscript, who have no competing 
interests). Editors highlight the need for all reviewers to be objective
 in their evaluation. </li><li>
<br></li><li>3- Any manuscripts received for review must be treated as 
confidential documents. They must not be shown to or discussed with 
others except as authorized by the editor. </li><li>
<br></li><li>4- The editor should be responsible for deciding which of the articles submitted to the journal should be published. </li><li>
<br></li><li>5- The editor may be guided by the policies of the journal's 
editorial board and constrained by such legal requirements, copyright 
infringement and plagiarism. </li><li>
<br></li><li>6- Editors should have no potential conflicts of interest with 
regard to the manuscript (with respect to the research, authors and/or 
research funders). </li><li>
<br></li><li>7- Editors and members of the editorial staff should not reveal
 the identity of reviewers prior to or following publication of the 
manuscript. </li><li>
<br></li></ul><h4 class="text-center">DUTIES AND RESPONSIBILITIES OF REVIEWERS</h4><li>1- Peer review assists the editor in making editorial decisions and 
through the editorial communications with the author may also assist the
 author in improving the paper. </li><li>
<br></li><li>2- Peer review is an essential component of formal scholarly 
communication. Authors who wish to contribute to publications have an 
obligation to do a fair share of reviewing. </li><li>
<br></li><li>3- Reviewers need to comment on ethical questions and possible research and publication misconduct. </li><li>
<br></li><li>4- Reviewers will do the work in timely manner and should notify editor if they cannot complete the work. </li><li>
<br></li><li>5- Reviewers need to keep the confidentiality of the manuscript. </li><li>
<br></li><li>6- Privileged information or ideas obtained through peer review must be kept confidential and not used for personal advantage</li><li>
<br></li><li>7- Reviews should be conducted objectively. Personal criticism 
of the author is inappropriate. Referees should express their views 
clearly with supporting arguments. </li><li>
<br></li><li>8- Reviewers should identify relevant published work that has 
not been cited by the authors. Any statement that is derived from the 
previously reported observation or argument should be accompanied by 
relevant citation. </li><li>
<br></li><li>9- A reviewer should also call to the editor's attention any 
substantial similarity or overlap between the manuscript under 
consideration and any other published paper of which they have personal 
knowledge</li><li>
<br></li><li>10-Any selected referee who feels unqualified to review the 
research reported in a manuscript or knows that its prompt review will 
be impossible should notify the editor and excuse him or herself from 
the review process. </li><li>

	<h4 class="text-left">AUTHORS RESPONSIBILITIES</h4>		 
			
1- The research being reported should have been conducted in an ethical 
and responsible manner and should comply with all relevant legislation</li><li>
<br></li><li>2- Researchers should present their results clearly, honestly, 
and without fabrication, falsification or inappropriate data 
manipulation</li><li>
<br></li><li>3- The corresponding author should ensure that all appropriate 
co-authors and no inappropriate co-authors are included on the paper, 
and that all co-authors have seen and approved the final version of the 
paper and have agreed to its submission for publication</li><li>
<br></li><li>4- Authorship should be limited to those who have made a 
significant contribution to the conception, design, execution, or 
interpretation of the reported study</li><li>
<br></li><li>5- All those who have made significant contributions should be listed as co-authors</li><li>
<br></li><li>6- Author(s) should not engage in plagiarism nor self-plagiarism. </li><li>
<br></li><li>7- Authors are required to agree with this open access policy 
which enables unrestricted access and reuse of all published articles. </li><li>
<br></li><li>8- A paper should contain sufficient detail and references to 
permit others to replicate the work. Fraudulent or knowingly inaccurate 
statements constitute unethical behaviour and are unacceptable. </li><li>
<br></li><li>9- Authors may be asked to provide the raw data in connection 
with a paper for editorial review. and should be prepared to provide 
public access to such data, if practicable, and should, in any event, be
 prepared to retain such data for a reasonable time after publication. 
The authors are responsible for data reproducibility. </li><li>
<br></li><li>10-The authors should ensure that they have written entirely 
original works, and if the authors have used the work and/or words of 
others, that this has been appropriately cited or quoted. </li><li>
<br></li><li>11-An author should not in general publish manuscripts 
describing essentially the same research in more than one journal or 
primary publication. Submitting the same manuscript to more than one 
journal concurrently constitutes unethical publishing behaviour and is 
unacceptable. In general, an author should not submit for consideration 
in another journal a previously published paper. </li><li>
<br></li><li>12-When an author discovers a significant error or inaccuracy 
in his/her own published work, it is the author´s obligation to promptly
 notify the journal editor or publisher and cooperate with the editor to
 retract or correct the paper. If the editor or the publisher learns 
from a third party that a published work contains a significant error, 
it is the obligation of the author to promptly retract or correct the 
paper or provide evidence to the editor of the correctness of the 
original paper. </li><li>


    
    <br></li><li>Editors and reviewers will ensure that articles submitted 
to the journal are original studies which have not been submitted 
anywhere else. Manuscripts will be fairly and objectively reviewed; 
authors will receive corrections and suggestions relatively quickly 
depending on the availability and expertise of reviewers.</li><li>
<br></li></ul>

				 
				<h4 class="text-left">DEALING WITH POTENTIAL MISCONDUCT</h4>	
				<p>Editors have a duty to take action if they suspect misconduct or 
if an allegation of misconduct is notified to them. This duty extends to
 both published and unpublished papers. Editors should not merely reject
 papers that raise concerns over potential misconduct. They are 
ethically obliged to pursue alleged cases.Editors should first seek a 
response from those suspected of misconduct. If they are not satisfied 
with the response, they should ask the relevant employers, or 
institution, or other appropriate body to investigate. Editors should 
make all reasonable efforts to ensure that a proper investigation into 
alleged misconduct is conducted; should the latter not take place, 
editors should make all reasonable attempts to obtain a resolution to 
the problem.
</p>
				<h4 class="text-left">PLAGIARISM</h4>	
				<p>Manuscript submissions are checked for intellectual property 
protection and document-versus-document(s) analysis to satisfy the 
Editor that the submitted manuscript has not been plagiarised. However, 
when authors submit their manuscripts for consideration in the Journal, 
they declare that their work is not plagiarised. While the Editor makes 
reasonable efforts to determine the academic integrity of papers 
published in the Journal, ultimate responsibility for the originality of
 submitted manuscripts thus lies with the author.</p><p>
				    
</p><p>Plagiarism takes place when one author deliberately uses 
another’s work without permission, credit, or acknowledgment. Authors 
must always remember that crediting the work of others (including your 
advisor’s or your own previous work) is paramount. Authors should always
 place their work in the context of the advancement of the field, and 
acknowledge the findings of others on which you have built your 
research.
</p>
				<h4 class="text-left">ETHICAL OVERSIGHT</h4>	
				<p>If the research work involves chemicals, humans, animals, 
procedures, or equipment that have any unusual hazards inherent in their
 use, the author must identify these in the manuscript to obey the 
ethical conduct of research using animals and human subjects. If 
required, the Authors must provide legal, ethical clearance from the 
association or legal organization.
				If the research involves confidential data and of business or 
marketing practices, authors should clearly justify this matter whether 
the data or information will be hidden securely or not.</p>
				

