				<p>If the research work involves chemicals, humans, animals, 
procedures, or equipment that have any unusual hazards inherent in their
 use, the author must identify these in the manuscript to obey the 
ethical conduct of research using animals and human subjects. If 
required, the Authors must provide legal, ethical clearance from the 
association or legal organization.
				If the research involves confidential data and of business or 
marketing practices, authors should clearly justify this matter whether 
the data or information will be hidden securely or not.</p>
				

