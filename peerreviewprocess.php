                         <p class="post-desc" align="justify"> 
                              <div><h4 class="marT20 "> PAPER SUBMISSION</h4>
		<ul class="list"><ul><li>1- The corresponding author should submit the paper to the journal online using link https://IJFER .com/register.php. </li><li>
<br></li><li>2- After submitting paper-id is sent to author.</li><li>
<br></li><li>3- The author will be guided by email for further process and be able to track the progress of the paper by using paper id. </li><li>
<br></li></ul><h4 class="marT20 "> EDITORIAL BOARD</h4><li>1- Editorial Board will check the plagiarism, format and composition
 of the paper comply with the Guide for Authors, to ensure it includes 
the required sections and styles. If the manuscript fails to meet one or
 more requirements, the Editorial Board will return it to the authors 
for amendments.</li><li>
<br></li><li>2- Submitted paper will be screened by the Editor-in-Chief 
whether the manuscript abides by the focus and scope of IJFER  journal.
 paper that do not meet the basic requirements will be rejected without 
further review. </li><li>
<br></li><li>3- If the manuscript meets all requirements, the EIC will 
assign it to an Reviewer with relevant expertise, who will be 
responsible for managing the peer review process. Reviewer may reject 
manuscripts that they deem highly unlikely to pass peer review.</li><li>
<br></li><li>4- Articles submitted for possible publication are subjected to a double-blind, peer review process. </li><li>
<br></li><li>5- Articles that are found suitable by Editor are then sent for
 review to two experts in the field of the paper. Reviewer of a paper 
are unknown to each other</li><li>
<br></li><li>6- The reviewers will rate several aspects of the manuscript, 
offer specific suggestions for improvement, and make a recommendation 
with regard to its suitability for publication.</li><li>
<br></li><li>7- The review is submitted to the journal, with a 
recommendation to accept or reject the paper or with a request for 
revision (major or minor), that should be well substantiated and 
justified.</li><li>
<br></li></ul>
<h4 class="marT20">The key parameters to be applied in the final evaluation of all types of submissions are</h4>
       

                  
<div class="condition-box"><li>Innovation (Is the manuscript original? Does it provide new evidence
 or ideas capable of furthering knowledge in the given social economic 
context?)</li><li>Quality (clarity, logic, English language and grammar, thoroughness, layout etc.)
</li><li>Relevance (interest to readers, not too specific, 
applicability, importance of the topic, impact on social life, economy, 
scientific community, etc.)

<h4 class="marT20 "> Four possible decisions may therefore be reached
</h4>
       
          <p>         
</p></li><li> Accept – the manuscript satisfies all publication parameters and is worthy of publication.
</li><li> Minor/Major Revisions – further revision of the manuscript is required in order to satisfy all parameters.
</li><li>Reject and Resubmission Suggested – the paper fails to satisfy 
key parameters and substantial revision of the manuscript is required to
 address its shortcomings
</li><li>Reject – the paper fails to satisfy key parameters and it is highly unlikely that further work can address its shortcomings.
<p> 




	</p><h4 class="marT20 "> CONFIRMATION OF THE DECISION AND COMMUNICATION TO THE AUTHORS</h4>		
 <p> All decisions are confirmed by the Editor-in-Chief prior to 
notification.
The Editorial Board sends a decision email to the corresponding author 
including any relevant reviewer comments. All comments and related files
 will be provided in an anonymised form. </p><p>
</p><p> Revision Stage-  Manuscripts that have been recommended for revision will be returned to the submitting author.</p>
<p> Final Steps- If paper is accepted then IJFER  journal makes every 
effort to ensure articles are published rapidly and accurately. the 
corresponding author will receive a paper acceptance e-mail and he/she 
should send Copyright form, then the article is published online under 
the licence Creative Commons Attribution – Non Commercial – No Derivs 
Alike (CC-BY-NC-ND) with in 4 hours after receiving required documents. 
Copyright for commercial and associated uses will remain with the 
Publisher.</p>

<p>After completing all the process by author paper/article is published
 online in respective volume/issue and same is mailed to author with 
certificate of publication</p>
<h4 class="marT20 "> FAIR PLAY</h4>		 
<p> Editor should evaluate manuscripts for their intellectual content 
without regard to race, gender, sexual orientation, religious belief, 
ethnic origin, citizenship, or political philosophy of the authors. 
Editors´ decision to accept or reject a paper for publication should be 
based only on the paper´s  importance, originality and clarity, and the 
study´s relevance to the aim of journal.</p><p>
</p><h4 class="marT20 ">COPYRIGHT POLICY</h4>
				 <p> Copyright policies are legal documents that specify who retains
 the right to use and distribute an original work. After submitting 
paper, authors are asked to submit copyright agreement to abide by an 
open access (CC-BY NC-ND). Under the terms of this agreement, authors 
retain ownership of the copyright of their articles. However, this 
agreement permits any user to reuse, download, print, extract, archive, 
and distribute the article, so long as appropriate credit is given to 
the authors and the source of the research work. The agreement ensures 
that the paper will be available as widely as possible and that the 
paper can be included in any IJFER  archive.
				 </p>

				 <h4 class="marT20 marB20">List of author rights in copyright policy:</h4>
				 <ul><li>  In future research work reuse idea or data of the paper .</li><li>  Reproduce incopies of the article for academic, coaching or teaching purposes.</li><li>  Use the published version of the article in presentations (conferences, seminars, etc.).</li><li>  Grant permission to others to use or re-use table or figure.</li></ul>
				 
				 <h4 class="marT20 marB30">List of all rights author(s) are granting to IJFER :</h4>
				 <ul><li> 	Reproduce, publish, distribute, edit and translate the work (specify either exclusively or non-exclusively).</li><li> 	Adapt and create derivatives from the work.</li><li> 	Make the article available in all forms and media (so article can be made available with any new technology).</li><li> 	Enforce copyright on behalf of the author.</li><li> 	Similarly to 
the author rights section, you’ll want to make the rights you expect the
 author(s) to grant to the journal explicit to avoid any confusion.</li></ul>
				 
